package Modelo;

/**
 *
 * @author Leo
 */
public class EmpresaHotelera {

    private int idEmpresaHotel;
    private String razonSocial;
    private String ruc;
    private String direccion;
    private String telefonoCentral;

    /**
     * @return the idEmpresaHotel
     */
    public int getIdEmpresaHotel() {
        return idEmpresaHotel;
    }

    /**
     * @param idEmpresaHotel the idEmpresaHotel to set
     */
    public void setIdEmpresaHotel(int idEmpresaHotel) {
        this.idEmpresaHotel = idEmpresaHotel;
    }

    /**
     * @return the razonSocial
     */
    public String getRazonSocial() {
        return razonSocial;
    }

    /**
     * @param razonSocial the razonSocial to set
     */
    public void setRazonSocial(String razonSocial) {
        this.razonSocial = razonSocial;
    }

    /**
     * @return the ruc
     */
    public String getRuc() {
        return ruc;
    }

    /**
     * @param ruc the ruc to set
     */
    public void setRuc(String ruc) {
        this.ruc = ruc;
    }

    /**
     * @return the direccion
     */
    public String getDireccion() {
        return direccion;
    }

    /**
     * @param direccion the direccion to set
     */
    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    /**
     * @return the telefonoCentral
     */
    public String getTelefonoCentral() {
        return telefonoCentral;
    }

    /**
     * @param telefonoCentral the telefonoCentral to set
     */
    public void setTelefonoCentral(String telefonoCentral) {
        this.telefonoCentral = telefonoCentral;
    }
}
